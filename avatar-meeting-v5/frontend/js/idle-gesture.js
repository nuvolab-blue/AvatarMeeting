/**
 * @fileoverview Body animation controller — pose-modulated noise.
 *
 * Instead of trying to directly retarget arm positions from pose landmarks
 * (which fails due to bone-local coordinate system mismatches), this system
 * uses pose data to MODULATE the intensity of Perlin-noise idle animation.
 *
 * When the user gestures more (arms moving away from rest), the noise
 * amplitude increases proportionally → avatar appears to react to body
 * language while always staying in natural, safe poses.
 *
 * Three activity signals:
 *  1. jawOpen  → speech intensity (existing from v7)
 *  2. arm activity → how far each arm deviates from resting position
 *  3. torso lean  → spine bones follow body lean (Euler, clamped)
 *
 * This guarantees:
 *  - Arms NEVER clip through the body
 *  - Poses are always anatomically valid (generated by noise within safe ranges)
 *  - More user movement → more avatar movement (natural correlation)
 */

import * as THREE from 'three';

// ============================================================================
// Simple 1D Perlin-like noise
// ============================================================================
class Noise1D {
  constructor(seed = 1) {
    this._p = new Uint8Array(512);
    const perm = new Uint8Array(256);
    for (let i = 0; i < 256; i++) perm[i] = i;
    let s = seed;
    for (let i = 255; i > 0; i--) {
      s = (s * 16807) % 2147483647;
      const j = s % (i + 1);
      [perm[i], perm[j]] = [perm[j], perm[i]];
    }
    for (let i = 0; i < 512; i++) this._p[i] = perm[i & 255];
  }

  _fade(t) { return t * t * t * (t * (t * 6 - 15) + 10); }
  _lerp(a, b, t) { return a + t * (b - a); }
  _grad(hash, x) {
    const h = hash & 15;
    const g = 1 + (h & 7);
    return ((h & 8) ? -g : g) * x;
  }

  noise(x) {
    const X = Math.floor(x) & 255;
    x -= Math.floor(x);
    const u = this._fade(x);
    return this._lerp(
      this._grad(this._p[X], x),
      this._grad(this._p[X + 1], x - 1),
      u
    ) * 0.5;
  }

  fbm(x, octaves = 3) {
    let v = 0, amp = 0.5, freq = 1;
    for (let i = 0; i < octaves; i++) {
      v += amp * this.noise(x * freq);
      amp *= 0.5;
      freq *= 2;
    }
    return v;
  }
}

